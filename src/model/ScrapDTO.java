package model;

public class ScrapDTO {
	private String scrap_seq;
	private String user_id;
	private String petition_seq;
	private String scrap_day;
	
	public String getScrap_seq() {
		return scrap_seq;
	}
	public void setScrap_seq(String scrap_seq) {
		this.scrap_seq = scrap_seq;
	}
	public String getUser_id() {
		return user_id;
	}
	public void setUser_id(String user_id) {
		this.user_id = user_id;
	}
	public String getPetition_seq() {
		return petition_seq;
	}
	public void setPetition_seq(String petition_seq) {
		this.petition_seq = petition_seq;
	}
	public String getScrap_day() {
		return scrap_day;
	}
	public void setScrap_day(String scrap_day) {
		this.scrap_day = scrap_day;
	}
	
}
