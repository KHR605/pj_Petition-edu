package model;

public class RippleDTO {
	private String ripple_seq;
	private String petition_seq;
	private String ripple_day;
	private String ripple_content;
	private String writer;
	
	public String getRipple_seq() {
		return ripple_seq;
	}
	public void setRipple_seq(String ripple_seq) {
		this.ripple_seq = ripple_seq;
	}
	public String getPetition_seq() {
		return petition_seq;
	}
	public void setPetition_seq(String petition_seq) {
		this.petition_seq = petition_seq;
	}
	public String getRipple_day() {
		return ripple_day;
	}
	public void setRipple_day(String ripple_day) {
		this.ripple_day = ripple_day;
	}
	public String getRipple_content() {
		return ripple_content;
	}
	public void setRipple_content(String ripple_content) {
		this.ripple_content = ripple_content;
	}
	public String getWriter() {
		return writer;
	}
	public void setWriter(String writer) {
		this.writer = writer;
	}
}
