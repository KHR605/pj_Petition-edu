package model;

public class ConsenterDTO {
	private String consenter_seq;
	private String consenter;
	private String petition_seq;
	private String agree_day;
	
	public String getConsenter_seq() {
		return consenter_seq;
	}
	public void setConsenter_seq(String consenter_seq) {
		this.consenter_seq = consenter_seq;
	}
	public String getConsenter() {
		return consenter;
	}
	public void setConsenter(String consenter) {
		this.consenter = consenter;
	}
	public String getPetition_seq() {
		return petition_seq;
	}
	public void setPetition_seq(String petition_seq) {
		this.petition_seq = petition_seq;
	}
	public String getAgree_day() {
		return agree_day;
	}
	public void setAgree_day(String agree_day) {
		this.agree_day = agree_day;
	}
	
	
}
